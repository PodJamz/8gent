// Skills Registry - Complete catalog of all 8gent capabilities
// Each skill is represented as a collectible game card

export type SkillMode = 'on' | 'off' | 'auto';
export type SkillRarity = 'common' | 'uncommon' | 'rare' | 'epic' | 'legendary';
export type SkillCategory = 'automation' | 'development' | 'design' | 'scientific' | 'management' | 'creative' | 'security' | 'ml';

export interface SkillDependency {
  name: string;
  type: 'cli' | 'npm' | 'api' | 'python' | 'native';
  installCommand?: string;
  url?: string;
}

export interface SkillSource {
  author: string;
  url?: string;
  license?: string;
  version?: string;
}

export interface SkillCommand {
  name: string;
  syntax: string;
  description: string;
}

export interface SkillDefinition {
  id: string;
  name: string;
  shortName: string; // For card display
  description: string;
  longDescription: string;
  category: SkillCategory;
  rarity: SkillRarity;
  icon: string; // Lucide icon name
  gradient: string; // Tailwind gradient classes
  accentColor: string; // HSL color for glow effects
  commands: SkillCommand[];
  dependencies: SkillDependency[];
  source?: SkillSource;
  tags: string[];
  stats: {
    power: number; // 1-100, how powerful/capable
    complexity: number; // 1-100, learning curve
    utility: number; // 1-100, how often useful
  };
  defaultMode: SkillMode;
}

