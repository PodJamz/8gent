export function getSkillById(id: string): SkillDefinition | undefined {
  return SKILLS_REGISTRY.find(skill => skill.id === id);
}

export function getSkillsByCategory(category: SkillCategory): SkillDefinition[] {
  return SKILLS_REGISTRY.filter(skill => skill.category === category);
}

export function getSkillsByRarity(rarity: SkillRarity): SkillDefinition[] {
  return SKILLS_REGISTRY.filter(skill => skill.rarity === rarity);
}

export function searchSkills(query: string): SkillDefinition[] {
  const lower = query.toLowerCase();
  return SKILLS_REGISTRY.filter(skill =>
    skill.name.toLowerCase().includes(lower) ||
    skill.description.toLowerCase().includes(lower) ||
    skill.tags.some(tag => tag.toLowerCase().includes(lower))
  );
}

